function max() {
    // console.log(arguments);
    var x = Array.from(arguments);
    var max = 0;
    // console.log(x);

    for(var i = 0; i < x.length; i += 1) {
        // console.log(x[i]);
        if(max < x[i]) {
            max = x[i];
        }
    }

    return max;
}

console.log(max(2, 3, 4, 8, 23, 678, 123));

